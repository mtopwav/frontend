// Storage key for language (section-scoped: systemLanguage_admin, etc.)
const getLanguageKey = (section) => (section ? `systemLanguage_${section}` : 'systemLanguage');

// Get current language from localStorage (section-scoped so admin/sales/finance/manager don't affect each other)
export const getCurrentLanguage = (section = null) => {
  return localStorage.getItem(getLanguageKey(section)) || 'en';
};

// Get a t function that uses the given section (for useTranslation)
export const getT = (section = null) => (key) => {
  const lang = getCurrentLanguage(section);
  return translations[lang]?.[key] ?? translations.en[key] ?? key;
};

// Legacy: translation for a key using global language only (prefer useTranslation + section)
export const t = (key) => getT(null)(key);

// Get all translations for current language (section-scoped)
export const getTranslations = (section = null) => {
  const lang = getCurrentLanguage(section);
  return translations[lang] || translations.en;
};
